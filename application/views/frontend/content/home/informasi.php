            <h3>Definisi TB Paru</h3>
            <p class='text-justify'>Tuberkulosis paru merupakan penyakit radang pada parenkim paru yang diakibatkan oleh infeksi Mycobacterium tuberculosis</p>
            <h4>Gejala TB Paru</h4>
            <h5>Gejala utama:</h5>
            <p>Batuk secara terus-menerus disertai dengan dahak selama tiga minggu atau lebih. </p>
            <h5>Gejala tambahan:</h5>
            <ol>
                <li>Dahak disertai dengan darah atau batuh darah</li>
                <li>Demam selama tiga minggu atau lebih</li>
                <li>Sesak nafas dan nyeri dada</li>
                <li>Nafsu makan menurun</li>
                <li>Berat badan menurun</li>
                <li>Kurang enak badan atau malaise</li>
                <li>Berkeringat di malam hari meskipun tidak beraktivitas</li>
                
            </ol>
            <h5>Faktor Risiko TB Paru</h5>
            <ol>
                <li>Orang dengan HIV positif dan penyakit imunokompromais lain</li>
                <li>Orang yang mengonsumsi obat imunosupresan dalam jangka waktu panjang</li>
                <li>Perokok</li>
                <li>Konsumsi alkohol tinggi</li>
                <li>Anak usia < 5 tahun dan lansia </li>
                <li>Memiliki kontak erat dengan orang dengan penyakit TB aktif yang infeksius</li>
                <li>Berada di tempat dengan risiko tinggi terinfeksi tuberkulosis (contoh: lembaga permasyarakatan, fasilitas perawatan jangka panjang)</li>
                <li>Petugas kesehatan.</li>
            </ol>
            <h5>Klasifikasi TB Paru</h5>
            <ol>
                <li>Klasifikasi berdasarkan lokasi anatomi
                    <ul>
                        <li>Tuberkulosis paru</li>
                        <li>Tuberkulosis ekstra paru</li>
                    </ul>
                </li>
                <li>Klasifikasi berdasarkan riwayat pengobatan
                    <ul>
                        <li>Kasus baru</li>
                        <li>Kasus dengan riwayat pengobatan</li>
                        <li>Kasus kambuh</li>
                        <li>Kasus pengobatan setelah gagal</li>
                        <li>Kasus setelah loss to follow up</li>
                        <li>Kasus lain-lain</li>
                        <li>Kasus dengan riwayat pengobatan tidak diketahui</li>
                    </ul>
                </li>
                <li>Klasifikasi berdasarkan hasil uji kepekaan obat
                    <ul>
                        <li>Monoresisten</li>
                        <li>Poliresisten</li>
                        <li>Multidrug resistant (TB MDR) </li>
                        <li>Extensive drug resistant (TB XDR) </li>
                        <li>Rifampicin resistant (TB RR)</li>
                    </ul>
                </li>
                <li>Klasifikasi berdasarkan status HIV
